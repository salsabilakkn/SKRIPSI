class ParticleSwarmOptimizer:
    def __init__(self, n_particles=20, n_iterations=100,
                 w=0.7, c_p=2.0, c_g=2.0, timestep=1):  # REVISI NAMA
        """
        PSO dengan nama parameter sesuai permintaan:
        - c_p: cognitive coefficient (percepatan pribadi)
        - c_g: social coefficient (percepatan global)
        """
        self.n_particles = n_particles
        self.n_iterations = n_iterations
        self.w = w          # inertia weight
        self.c_p = c_p      # cognitive coefficient (sesuai permintaan)
        self.c_g = c_g      # social coefficient (sesuai permintaan)
        self.timestep = timestep

        # Rentang hyperparameter sesuai permintaan
        self.param_ranges = {
            'units': (8, 128),
            'learning_rate': (1e-5, 0.01),
            'batch_size': (8, 128),
            'dropout': (0.0, 0.3)
        }

        self.best_global_position = None
        self.best_global_value = float('inf')
        self.particles = []
        self.history = []

    def initialize_particles(self):
        """Inisialisasi partikel dengan posisi acak"""
        for i in range(self.n_particles):
            particle = {
                'id': i + 1,
                'position': {},
                'velocity': {},
                'best_position': {},
                'best_value': float('inf')
            }

            for param, (min_val, max_val) in self.param_ranges.items():
                if param in ['units', 'batch_size']:
                    # Integer parameters
                    particle['position'][param] = random.randint(int(min_val), int(max_val))
                    particle['velocity'][param] = random.uniform(-1, 1)
                else:
                    # Float parameters
                    particle['position'][param] = random.uniform(min_val, max_val)
                    particle['velocity'][param] = random.uniform(-0.1, 0.1)

            particle['best_position'] = particle['position'].copy()
            self.particles.append(particle)

    def evaluate_particle(self, position, X_train, y_train, X_val, y_val):
        """Evaluasi fitness particle dengan MAPE"""
        try:
            model = build_gru_model(
                units=int(position['units']),
                dropout=position['dropout'],
                learning_rate=position['learning_rate'],
                timestep=self.timestep
            )

            # Training cepat untuk evaluasi fitness
            model.fit(
                X_train, y_train,
                epochs=3,
                batch_size=int(position['batch_size']),
                validation_data=(X_val, y_val),
                verbose=0
            )

            mape, _, _ = evaluate_model(model, X_val, y_val, scaler)
            return mape if not np.isnan(mape) else float('inf')

        except Exception as e:
            return float('inf')

    def optimize(self, X_train, y_train, X_val, y_val):
        """Proses optimasi PSO"""
        print(f"Memulai optimasi PSO dengan {self.n_particles} partikel, {self.n_iterations} iterasi...")
        print(f"Parameter: w={self.w}, c_p={self.c_p}, c_g={self.c_g}")

        # 1. Inisialisasi partikel
        self.initialize_particles()

        # 2. Proses iterasi
        for iteration in range(self.n_iterations):
            # Tampilkan progress setiap 10 iterasi
            if (iteration + 1) % 10 == 0:
                print(f"\rIterasi {iteration+1}/{self.n_iterations} | Best MAPE: {self.best_global_value:.4f}%", end="")

            # 3. Evaluasi semua partikel
            for particle in self.particles:
                current_value = self.evaluate_particle(
                    particle['position'], X_train, y_train, X_val, y_val
                )

                # Update personal best
                if current_value < particle['best_value']:
                    particle['best_value'] = current_value
                    particle['best_position'] = particle['position'].copy()

                # Update global best
                if current_value < self.best_global_value:
                    self.best_global_value = current_value
                    self.best_global_position = particle['position'].copy()

            # 4. Update velocity dan position dengan c_p dan c_g
            for particle in self.particles:
                r1 = random.random()
                r2 = random.random()

                for param in self.param_ranges.keys():
                    # Cognitive component dengan c_p
                    cognitive = self.c_p * r1 * (particle['best_position'][param] - particle['position'][param])

                    # Social component dengan c_g
                    social = self.c_g * r2 * (self.best_global_position[param] - particle['position'][param])

                    # Update velocity
                    particle['velocity'][param] = (
                        self.w * particle['velocity'][param] + cognitive + social
                    )

                    # Update position
                    new_position = particle['position'][param] + particle['velocity'][param]

                    # Clamp ke batas yang ditentukan
                    min_val, max_val = self.param_ranges[param]
                    new_position = max(min_val, min(max_val, new_position))

                    if param in ['units', 'batch_size']:
                        particle['position'][param] = int(round(new_position))
                    else:
                        particle['position'][param] = new_position

            # 5. Simpan history
            self.history.append({
                'iteration': iteration + 1,
                'best_value': self.best_global_value,
                'best_position': self.best_global_position.copy()
            })

        print(f"\n\n✅ Optimasi PSO selesai!")
        print(f"Best MAPE selama optimasi: {self.best_global_value:.4f}%")
        print(f"Parameter terbaik:")
        print(f"  - units: {int(self.best_global_position['units'])}")
        print(f"  - learning_rate: {self.best_global_position['learning_rate']:.6f}")
        print(f"  - batch_size: {int(self.best_global_position['batch_size'])}")
        print(f"  - dropout: {self.best_global_position['dropout']:.3f}")

        return self.best_global_position, self.best_global_value

