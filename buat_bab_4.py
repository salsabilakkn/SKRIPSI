# Fungsi untuk prediksi multi-step
def predict_future(model, last_sequence, scaler, n_days=5):
    """
    Prediksi n hari ke depan menggunakan model GRU

    Parameters:
    - model: model GRU yang sudah ditrain
    - last_sequence: sequence terakhir dari data training (shape: [1, timestep, 1])
    - scaler: scaler untuk inverse transform
    - n_days: jumlah hari yang akan diprediksi
    """
    predictions = []
    current_sequence = last_sequence.copy()

    for day in range(n_days):
        # Prediksi 1 hari ke depan
        pred_scaled = model.predict(current_sequence, verbose=0)

        # Inverse transform untuk mendapatkan nilai asli
        pred_original = scaler.inverse_transform(pred_scaled.reshape(-1, 1)).flatten()[0]
        predictions.append(pred_original)

        # Update sequence untuk prediksi berikutnya
        if TIMESTEP > 1:
            # Geser sequence: hilangkan nilai pertama, tambahkan prediksi terakhir
            current_sequence = np.roll(current_sequence, -1, axis=1)
            current_sequence[0, -1, 0] = pred_scaled[0, 0]
        else:
            # Jika timestep=1, ganti seluruh sequence dengan prediksi terakhir
            current_sequence[0, 0, 0] = pred_scaled[0, 0]

        print(f"   Prediksi hari {day+1}: {pred_original:.2f} (scaled: {pred_scaled[0,0]:.4f})")

    return np.array(predictions)

