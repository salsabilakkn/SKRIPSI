def prepare_data_all_models(data, seq_length=10):
    """
    Mempersiapkan data untuk semua model dengan strategi splitting yang benar
    Urutan: Normalisasi → Split → Windowing
    Menggunakan data yang sudah di-winsorize
    """
    print(f"\nMenggunakan data dengan {len(data)} samples")
    print(f"Data min: {data.min():.2f}, max: {data.max():.2f}, mean: {data.mean():.2f}")

    # 1. NORMALISASI DATA UTUH (data yang sudah di-winsorize)
    scaler = MinMaxScaler(feature_range=(0, 1))
    data_scaled = scaler.fit_transform(data.reshape(-1, 1))

    # 2. SPLIT PERTAMA: 80% train_total, 20% test (untuk semua model)
    total_samples = len(data_scaled)
    split1_idx = int(total_samples * 0.8)  # 80% untuk training total

    train_total = data_scaled[:split1_idx]  # 80% dari total
    test_all = data_scaled[split1_idx:]     # 20% dari total (sama untuk semua model)

    print(f"\nSplit 1 - 80:20:")
    print(f"  Train total: {len(train_total)} samples ({len(train_total)/total_samples*100:.1f}%)")
    print(f"  Test (semua model): {len(test_all)} samples ({len(test_all)/total_samples*100:.1f}%)")

    # 3. SPLIT KEDUA: Dari 80% train_total, bagi jadi 80% train_final, 20% validation
    split2_idx = int(len(train_total) * 0.8)  # 80% dari train_total

    train_final = train_total[:split2_idx]    # 64% dari total (80% dari 80%)
    val_final = train_total[split2_idx:]      # 16% dari total (20% dari 80%)

    print(f"\nSplit 2 - Dari 80% train_total bagi 80:20:")
    print(f"  Train final: {len(train_final)} samples ({len(train_final)/total_samples*100:.1f}%)")
    print(f"  Validation: {len(val_final)} samples ({len(val_final)/total_samples*100:.1f}%)")

    # 4. WINDOWING untuk setiap subset
    def create_windows(data_subset, seq_length):
        X, y = [], []
        for i in range(len(data_subset) - seq_length):
            X.append(data_subset[i:i+seq_length])
            y.append(data_subset[i+seq_length])
        return np.array(X), np.array(y)

    # Windowing untuk train_final
    X_train, y_train = create_windows(train_final, seq_length)

    # Windowing untuk val_final
    X_val, y_val = create_windows(val_final, seq_length)

    # Windowing untuk test_all
    X_test, y_test = create_windows(test_all, seq_length)

    # 5. RESHAPE untuk GRU [samples, timesteps, features]
    X_train = X_train.reshape((X_train.shape[0], seq_length, 1))
    X_val = X_val.reshape((X_val.shape[0], seq_length, 1))
    X_test = X_test.reshape((X_test.shape[0], seq_length, 1))

    print(f"\nShape setelah windowing (seq_length={seq_length}):")
    print(f"  X_train: {X_train.shape}, y_train: {y_train.shape}")
    print(f"  X_val: {X_val.shape}, y_val: {y_val.shape}")
    print(f"  X_test: {X_test.shape}, y_test: {y_test.shape}")

    return {
        'X_train': X_train, 'y_train': y_train,
        'X_val': X_val, 'y_val': y_val,
        'X_test': X_test, 'y_test': y_test,
        'scaler': scaler,
        'train_total': train_total,  # untuk baseline
        'test_all': test_all
    }

