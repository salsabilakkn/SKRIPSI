class AdaptivePSO:
    def __init__(self, n_particles=20, n_iterations=100,
                 w_initial=0.7, w_max=0.9, w_min=0.4,
                 c_max=2.5, c_min=1.5, timestep=1):
        """
        APSO dengan parameter adaptif:
        - w: linear decreasing dari w_max ke w_min
        - c_p: decreasing dari c_max ke c_min (cognitive)
        - c_g: increasing dari c_min ke c_max (social)
        """
        self.n_particles = n_particles
        self.n_iterations = n_iterations
        self.w_initial = w_initial
        self.w_max = w_max
        self.w_min = w_min
        self.c_max = c_max  # nilai maksimum untuk c_p dan c_g
        self.c_min = c_min  # nilai minimum untuk c_p dan c_g
        self.timestep = timestep

        # Rentang hyperparameter tetap sama
        self.param_ranges = {
            'units': (8, 128),
            'learning_rate': (1e-5, 0.01),
            'batch_size': (8, 128),
            'dropout': (0.0, 0.3)
        }

        self.best_global_position = None
        self.best_global_value = float('inf')
        self.particles = []
        self.history = []

    def calculate_adaptive_parameters(self, iteration):
        """Hitung parameter adaptif berdasarkan iterasi"""
        progress = iteration / self.n_iterations

        # Inertia weight: linear decreasing dari w_max ke w_min
        w_current = self.w_max - (self.w_max - self.w_min) * progress

        # c_p: decreasing dari c_max ke c_min (cognitive)
        c_p_current = self.c_max - (self.c_max - self.c_min) * progress

        # c_g: increasing dari c_min ke c_max (social)
        c_g_current = self.c_min + (self.c_max - self.c_min) * progress

        return w_current, c_p_current, c_g_current

    def initialize_particles(self):
        """Inisialisasi partikel"""
        for i in range(self.n_particles):
            particle = {
                'id': i + 1,
                'position': {},
                'velocity': {},
                'best_position': {},
                'best_value': float('inf')
            }

            for param, (min_val, max_val) in self.param_ranges.items():
                if param in ['units', 'batch_size']:
                    particle['position'][param] = random.randint(int(min_val), int(max_val))
                    particle['velocity'][param] = random.uniform(-1, 1)
                else:
                    particle['position'][param] = random.uniform(min_val, max_val)
                    particle['velocity'][param] = random.uniform(-0.1, 0.1)

            particle['best_position'] = particle['position'].copy()
            self.particles.append(particle)

    def evaluate_particle(self, position, X_train, y_train, X_val, y_val):
        """Evaluasi fitness particle"""
        try:
            model = build_gru_model(
                units=int(position['units']),
                dropout=position['dropout'],
                learning_rate=position['learning_rate'],
                timestep=self.timestep
            )

            model.fit(
                X_train, y_train,
                epochs=3,
                batch_size=int(position['batch_size']),
                validation_data=(X_val, y_val),
                verbose=0
            )

            mape, _, _ = evaluate_model(model, X_val, y_val, scaler)
            return mape if not np.isnan(mape) else float('inf')

        except Exception as e:
            return float('inf')

    def optimize(self, X_train, y_train, X_val, y_val):
        """Proses optimasi APSO"""
        print(f"Memulai optimasi APSO dengan {self.n_particles} partikel, {self.n_iterations} iterasi...")
        print(f"Parameter adaptif:")
        print(f"  - w: {self.w_initial} (initial), decreasing {self.w_max} → {self.w_min}")
        print(f"  - c_p: decreasing {self.c_max} → {self.c_min} (cognitive)")
        print(f"  - c_g: increasing {self.c_min} → {self.c_max} (social)")

        self.initialize_particles()

        for iteration in range(self.n_iterations):
            # Dapatkan parameter adaptif untuk iterasi ini
            w_current, c_p_current, c_g_current = self.calculate_adaptive_parameters(iteration)

            if (iteration + 1) % 10 == 0:
                print(f"\rIterasi {iteration+1}/{self.n_iterations} | "
                      f"w: {w_current:.3f}, c_p: {c_p_current:.3f}, c_g: {c_g_current:.3f} | "
                      f"Best MAPE: {self.best_global_value:.4f}%", end="")

            # Evaluasi partikel
            for particle in self.particles:
                current_value = self.evaluate_particle(
                    particle['position'], X_train, y_train, X_val, y_val
                )

                if current_value < particle['best_value']:
                    particle['best_value'] = current_value
                    particle['best_position'] = particle['position'].copy()

                if current_value < self.best_global_value:
                    self.best_global_value = current_value
                    self.best_global_position = particle['position'].copy()

            # Update velocity dan position dengan c_p_current dan c_g_current
            for particle in self.particles:
                r1 = random.random()
                r2 = random.random()

                for param in self.param_ranges.keys():
                    cognitive = c_p_current * r1 * (particle['best_position'][param] - particle['position'][param])
                    social = c_g_current * r2 * (self.best_global_position[param] - particle['position'][param])

                    particle['velocity'][param] = w_current * particle['velocity'][param] + cognitive + social

                    new_position = particle['position'][param] + particle['velocity'][param]
                    min_val, max_val = self.param_ranges[param]
                    new_position = max(min_val, min(max_val, new_position))

                    if param in ['units', 'batch_size']:
                        particle['position'][param] = int(round(new_position))
                    else:
                        particle['position'][param] = new_position

            # Simpan history dengan nama parameter yang benar
            self.history.append({
                'iteration': iteration + 1,
                'best_value': self.best_global_value,
                'best_position': self.best_global_position.copy(),
                'w': w_current,
                'c_p': c_p_current,  # nama sesuai permintaan
                'c_g': c_g_current   # nama sesuai permintaan
            })

        print(f"\n\n✅ Optimasi APSO selesai!")
        print(f"Best MAPE selama optimasi: {self.best_global_value:.4f}%")
        print(f"Parameter terbaik:")
        print(f"  - units: {int(self.best_global_position['units'])}")
        print(f"  - learning_rate: {self.best_global_position['learning_rate']:.6f}")
        print(f"  - batch_size: {int(self.best_global_position['batch_size'])}")
        print(f"  - dropout: {self.best_global_position['dropout']:.3f}")

        return self.best_global_position, self.best_global_value

print("✅ Class APSO dengan parameter c_p dan c_g sudah didefinisikan")

