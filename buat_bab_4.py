def calculate_mape(y_true, y_pred):
    """Menghitung Mean Absolute Percentage Error (MAPE)"""
    y_true, y_pred = np.array(y_true), np.array(y_pred)
    non_zero_indices = y_true != 0
    if np.sum(non_zero_indices) == 0:
        return np.nan
    return np.mean(np.abs((y_true[non_zero_indices] - y_pred[non_zero_indices]) /
                          y_true[non_zero_indices])) * 100

def evaluate_model(model, X_test, y_test, scaler):
    """Evaluasi model dan return metrics"""
    y_pred_scaled = model.predict(X_test, verbose=0)
    y_test_inv = scaler.inverse_transform(y_test.reshape(-1, 1)).flatten()
    y_pred_inv = scaler.inverse_transform(y_pred_scaled.reshape(-1, 1)).flatten()
    mape = calculate_mape(y_test_inv, y_pred_inv)
    return mape, y_test_inv, y_pred_inv

